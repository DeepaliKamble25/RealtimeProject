package bitWiseOperator;

public class DigitDemo {
	
	public static void main(String[] args) {
		
		System.out.println(4 & 5);//4
		/* 4=100
		 * 5=101
		 * ==100==4
		 */
		System.out.println(4 | 5);//5
		/* 4=100
		 * 5=101
		 * ==101==5
		 */
		System.out.println(4 ^ 5);//1
		/* 4=100
		 * 5=101
		 * ==001==1
		 */
	}

}
// Apply to Integral Type also==== byte, Short, int, Long
