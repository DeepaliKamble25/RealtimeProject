package typeCastingOperator;

public class ImplicitDemo {

	public static void main(String[] args) 
	{
		
	
		
		int x='a';
		
		System.out.println(x);
		
		/*compiler convert char to int type by implicit type casting*/
		
		double d=10;
		
		System.out.println(d);
		
		/*compiler convert int to double type by implicit type casting*/
		
	}
}	
	

